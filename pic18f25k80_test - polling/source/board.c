/*
*------------------------------------------------------------------------------
* void InitializeBoard(void)

* Summary	: This function configures all i/o pin directions
*
* Input		: None
*
* Output	: None
*
*------------------------------------------------------------------------------
*/
void BRD_init(void)
{
	//Internal oscillator 16MHz
	OSCCON = 0x70;

	DelayMs(5);

	// Disable ADC
	ADCON0 = 0x00;
	ADCON1 = 0x00;
	ADCON2 = 0x00;

	//Set analog pins as DIGITAL I/O
	ANCON1 = 0x00;
	ANCON0 = 0X00;

	// Configure heart beat LED output
	HEART_BEAT_DIR 	= PORT_OUT;
	HEART_BEAT 		= SWITCH_OFF;

	// Configure Serial port
	SER2_TX_DIR 		=  PORT_OUT;
	SER2_RX_DIR 		=  PORT_IN;

	SER1_TX_DIR 		=  PORT_OUT;
	SER1_RX_DIR 		=  PORT_IN;


	CAN_TX_DIR          =  PORT_OUT;
	CAN_RX_DIR			=  PORT_IN;


	OSCTUNEbits.PLLEN = 1;
	DelayMs(100);

}

	



